package typeCasting;

public class TypeCasting {

	public static void main(String[] args) {

		int x = 5;
		double y = x;
		float z = x;
		int a = 2;
		float b = (int)a; // this for manual type casting (upper two step)
		System.out.println(b);
		System.out.println(z);
		System.out.println(y);

	}

} 
